test_that("bioclims", {
  require(raster)
  tas <- stack(brick(array(rnorm(12e2), dim = c(10, 10, 12))))
  tmin <- stack(brick(array(rnorm(12e2), dim = c(10, 10, 12))))
  tmax <- stack(brick(array(rnorm(12e2), dim = c(10, 10, 12))))
  pr <- stack(brick(array(runif(12e2, 1e2, 1e4), dim = c(10, 10, 12))))
  names(tas) <- as.character(1:12)
  names(tmin) <- as.character(1:12)
  names(tmax) <- as.character(1:12)
  names(pr) <- as.character(1:12)
  expect_no_error(bios <- bioclim(tas, tmin, tmax, pr))
  expect_identical(names(bios), c(paste0("BIO0", 1:9), paste0("BIO", 10:19)))
})
